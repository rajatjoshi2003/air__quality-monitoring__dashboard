// ── Query Functions ───────────────────────────────────────

/** Returns monthly data for a given city, parameter, and year range. */
function getCPCBMonthly(cityId, param, yearFrom = 2019, yearTo = 2023) {
  const cityData = CPCB_MONTHLY[cityId];
  if (!cityData || !cityData[param]) return null;

  const result = [];
  const months = ['Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'];

  for (let y = yearFrom; y <= yearTo; y++) {
    const row = cityData[param][y];
    if (!row) continue;
    row.forEach((val, mIdx) => {
      result.push({
        year: y,
        month: mIdx + 1,
        monthLabel: months[mIdx],
        label: `${months[mIdx]} ${y}`,
        value: val,
        parameter: param,
        city: cityId,
      });
    });
  }
  return result;
}

/** Computes annual mean for a city, parameter, and year. */
function getCPCBAnnualMean(cityId, param, year) {
  const row = CPCB_MONTHLY[cityId]?.[param]?.[year];
  if (!row) return null;
  return +(row.reduce((a, b) => a + b, 0) / row.length).toFixed(1);
}

/**
 * Returns the best-estimate concentration for a given city/param
 * at a specific Date object, using monthly means and smooth interpolation.
 */
function getCPCBValueAt(cityId, param, date) {
  const year  = date.getFullYear();
  const month = date.getMonth(); // 0-11
  const day   = date.getDate();

  const useYear = Math.min(2023, Math.max(2019, year));
  const row = CPCB_MONTHLY[cityId]?.[param]?.[useYear];
  if (!row) return null;

  // Linear interpolation between this month and next
  const daysInMonth = new Date(year, month + 1, 0).getDate();
  const t = (day - 1) / daysInMonth;
  const thisVal = row[month];
  const nextVal = row[(month + 1) % 12];
  return +(thisVal + t * (nextVal - thisVal)).toFixed(2);
}

/** Returns a multi-city summary for a given year with annual means. */
function getCPCBCitySummary(year = 2023) {
  const params = ['pm25','pm10','no2','so2','o3','co'];
  return Object.keys(CPCB_MONTHLY).map(cityId => {
    const entry = { cityId, name: CITIES[cityId]?.name || cityId };
    params.forEach(p => {
      entry[p] = getCPCBAnnualMean(cityId, p, year);
    });
    return entry;
  });
}

/** Trend direction for a city/param from 2019 to 2023 (% change). */
function getCPCBTrend(cityId, param) {
  const v2019 = getCPCBAnnualMean(cityId, param, 2019);
  const v2023 = getCPCBAnnualMean(cityId, param, 2023);
  if (!v2019 || !v2023) return null;
  return +((((v2023 - v2019) / v2019) * 100).toFixed(1));
}

/** Returns station list for a city. */
function getCPCBStations(cityId) {
  return CPCB_STATIONS[cityId] || [];
}

