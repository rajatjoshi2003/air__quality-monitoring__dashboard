def calc_aqi(value: float, param: str = "pm25") -> tuple[int | None, str]:
    """Return (aqi_value, category) or (None, 'Unknown') if no breakpoints."""
    bp = AQI_BREAKPOINTS.get(param)
    if not bp or value is None:
        return None, "Unknown"
    for c_lo, c_hi, i_lo, i_hi, cat, _ in bp:
        if c_lo <= value <= c_hi:
            aqi = round((i_hi - i_lo) / (c_hi - c_lo) * (value - c_lo) + i_lo)
            return aqi, cat
    if value > bp[-1][1]:
        return 500, "Hazardous"
    return 0, "Good"
